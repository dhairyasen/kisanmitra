def get_translation(lang: str, key: str, **kwargs) -> str:
    """Get translated text for a given language and key."""
    lang = lang if lang in TRANSLATIONS else "hi"
    text = TRANSLATIONS[lang].get(key, TRANSLATIONS["hi"].get(key, key))
    if kwargs:
        try:
            text = text.format(**kwargs)
        except KeyError:
            pass
    return text

def get_weather_message(lang: str, temp: float, rain: float, wind: float) -> str:
    if rain > 20:
        return get_translation(lang, "weather_rain")
    if temp > 38:
        return get_translation(lang, "weather_hot")
    if temp < 10:
        return get_translation(lang, "weather_cold")
    if wind > 50:
        return get_translation(lang, "weather_wind")
    return get_translation(lang, "weather_normal")

def get_weather_advice(lang: str, temp: float, rain: float, wind: float) -> str:
    if rain > 20:
        return get_translation(lang, "advice_rain")
    if temp > 38:
        return get_translation(lang, "advice_hot")
    if wind > 50:
        return get_translation(lang, "advice_wind")
    return get_translation(lang, "advice_normal")
